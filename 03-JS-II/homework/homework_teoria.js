//Que es &&?

//Se utiliza para comprobar que dos condicionales sean verdaderas y se ejecutara solamente 
//si ambas son verdaderas, por ejemplo, si tenemos 2 colores para conseguir morado, unicamente 
//obtendremos morado cuando sean rojo y azul, si son otros colores no podremos hacer el morado

//Que es ||?

//Esta se entiende como un "y" o "and", la diferencia del "&&" es que en este, solamente se necesita 
//que una de las condicionales sea verdadera,por ejemplo si nos van a dar dos colores al azar
// y establecemos que si alguno de esos dos es morado o rojo, y resulta que el primero fue efectivamente
//morado a pesar de que el otro haya sido amarillo, el bloque de codigo o la funcion correra

